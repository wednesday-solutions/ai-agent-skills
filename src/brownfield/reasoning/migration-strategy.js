/**
 * 2D-2 — Migration strategy
 * Cross-service / cross-dependency upgrade plans.
 * Input: graph + summaries — never raw source.
 */

'use strict';

const fs = require('fs');
const path = require('path');
const https = require('https');

function buildMigrationPrompt(goal, graph, summaries) {
  const stats = graph.stats;
  const nodes = Object.values(graph.nodes);
  const affectedNodes = nodes
    .filter(n => n.riskScore > 30)
    .sort((a, b) => b.riskScore - a.riskScore)
    .slice(0, 12);

  const nodeList = affectedNodes.map(n =>
    `${n.file} (risk:${n.riskScore}): ${(summaries[n.file] || '').split('.')[0]}`
  ).join('\n');

  return `Migration goal: "${goal}"

Codebase: ${stats.totalFiles} files, ${JSON.stringify(stats.byLang)} languages

High-risk affected files:
${nodeList}

Produce a phased migration strategy:
- Phase 1: Preparation (backwards-compatible changes)
- Phase 2: Migration (incremental — 5–6 files per PR)
- Phase 3: Cleanup (remove compatibility shims)
Each phase: list specific files to touch, risks, and rollback strategy.`;
}

async function planMigration(goal, graph, summaries, refactorDir, apiKey) {
  const prompt = buildMigrationPrompt(goal, graph, summaries);

  let strategy = null;
  if (apiKey) {
    strategy = await callSonnet(prompt, apiKey);
  }

  if (!strategy) {
    strategy = `## Migration: ${goal}\n\n*API key not configured — run with OPENROUTER_API_KEY for AI-powered strategy*`;
  }

  const timestamp = new Date().toISOString().replace(/[:.]/g, '-').slice(0, 19);
  const filename = `migration-${timestamp}.md`;
  const content = `# Migration Strategy — ${goal}
Generated: ${new Date().toISOString()}

${strategy}

---
*Generated by wednesday-skills from graph analysis — not raw source code*
`;

  fs.mkdirSync(refactorDir, { recursive: true });
  const outPath = path.join(refactorDir, filename);
  fs.writeFileSync(outPath, content);
  return { strategy, outPath };
}

async function callSonnet(prompt, apiKey) {
  return new Promise((resolve) => {
    const body = JSON.stringify({
      model: 'anthropic/claude-sonnet-4-6',
      messages: [{ role: 'user', content: prompt }],
      max_tokens: 1000,
      temperature: 0,
    });

    const options = {
      hostname: 'openrouter.ai',
      path: '/api/v1/chat/completions',
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${apiKey}`,
        'Content-Length': Buffer.byteLength(body),
      },
    };

    const req = https.request(options, res => {
      let data = '';
      res.on('data', c => { data += c; });
      res.on('end', () => {
        try {
          const json = JSON.parse(data);
          resolve(json.choices?.[0]?.message?.content?.trim() || null);
        } catch { resolve(null); }
      });
    });
    req.on('error', () => resolve(null));
    req.setTimeout(60000, () => { req.destroy(); resolve(null); });
    req.write(body);
    req.end();
  });
}

module.exports = { planMigration };
